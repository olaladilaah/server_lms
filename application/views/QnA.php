	function createHtmlQnA(q, a, id)
	{
		return (
          "<div class=\"panel panel-default\">" +
          "    <div class=\"panel-heading\">" +
          "        <h4 class=\"panel-title\">" +
          "            <a class=\"accordion-toggle\" data-toggle=\"collapse\" data-parent=\"#accordion\" href=\"#collapse"+id+"\">"+q+"</a>" +
          "        </h4>" +
          "    </div>" +
          "    <div id=\"collapse"+id+"\" class=\"panel-collapse collapse\">" +
          "        <div class=\"panel-body\">" +
          "            " + marked(a) +
          "        </div>" +
          "    </div>" +
          "</div>\n");
	}

	$(document).ready(function(){
		var cArray = [];
		for(i in QnA)
		{
			var cItem = {};
			var indexCategory = -1;
			for(j in cArray)
			{
				if (cArray[j].text == QnA[i].category) {
					indexCategory = j;
					break;
				}
			}
			
			if (indexCategory == -1) {
				cItem.text = QnA[i].category;
				cItem.html = createHtmlQnA(QnA[i].question, QnA[i].answer, i);
				cArray.push(cItem);
			} else {
				cArray[indexCategory].html += createHtmlQnA(QnA[i].question, QnA[i].answer, i);
			}
		}
		
		var html = "";
		
		for(i in cArray)
		{
			html += "<div class=\"QnAHeader\">"+cArray[i].text+"</div>\n" + cArray[i].html + "<br />"
		}
		
		$("#accordion").html($(html));
		$(".accordion-toggle:first").click();
	});
</script>

